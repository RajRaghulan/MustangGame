import UIKit

/// Enumeration of a Move with all possible cases
enum Move {
    
    /// Move in the left direction
    case left
    
    /// Move in the right direction
    case right
    
    /// Move in the upward direction
    case up
    
    /// Move in the downward direction
    case down
    
    /// Symbolizes the end of a 'run' in the game
    case end
    
}
